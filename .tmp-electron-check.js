const path = require('path');
const fs = require('fs');
console.log('STEP: start');
const { SecureStorageService } = require('./electron/services/secure-storage');
console.log('STEP: secure-storage module');
const { InstallationIdentityService } = require('./electron/services/installation-identity');
console.log('STEP: installation-identity module');
const { DatabaseService } = require('./electron/services/database');
console.log('STEP: database module');
const base = path.join(process.cwd(), '.tmp-electron-boot');
const userDataPath = path.join(base, 'user');
const programDataPath = path.join(base, 'program');
const documentsPath = path.join(base, 'docs');
fs.mkdirSync(userDataPath, { recursive: true });
fs.mkdirSync(programDataPath, { recursive: true });
fs.mkdirSync(documentsPath, { recursive: true });
const secureStorage = new SecureStorageService({ userDataPath, programDataPath, appName: 'Kwanza Folha' });
console.log('STEP: secure-storage ready', secureStorage.dpapiAvailable);
const installationIdentity = new InstallationIdentityService({ userDataPath, secureStorage, appName: 'Kwanza Folha', productCode: 'KWANZAFOLHA', programDataPath });
console.log('STEP: installation-identity ready');
const identity = installationIdentity.verifyInstallationAnchors();
console.log('STEP: identity verified', JSON.stringify(identity));
const db = new DatabaseService(userDataPath, documentsPath, { programDataPath, secureStorage, installationIdentity });
console.log('STEP: database ready', !!db.db);
db.prepareForShutdown();
console.log('STEP: done');
process.exit(0);
